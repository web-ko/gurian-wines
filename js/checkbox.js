// If check box checked disable other, if unchecked enable all value უნდა ქონდეს ერთი და იგივე, name უნდა ქონდეს სკრიპტის მიხედვით 
$("input[name='check[]']").on('click', function() {
	var val = $(this).val();
	var parent = $(this);
  $("input[value='"+val+"']").each(function() {
  	$(this).not(parent).attr('disabled', parent.is(':checked'));
  });
})
// end of If check box checked disable other, if unchecked enable all



